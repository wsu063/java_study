package ch02.sec02;

public class IntegerLiteralExample {
    public static void main(String[] args) {
        //2진수 1011
        int var = 0b1011;
        System.out.println(var);

        //8진수
        int var2 = 0206;
        System.out.println(var2);

        //16진수
        int var3 = 0xB3; //
        System.out.println(var3);


    }
}
